class operator
{
	public static void main(String s[])
	{	
		int num1=31,num2=18;
		System.out.println(+num1+" + "+num2+" = "+(num1+num2));
		System.out.println(+num1+" - "+num2+" = "+(num1-num2));
		System.out.println(+num1+" * "+num2+" = "+(num1*num2));
		System.out.println(+num1+" / "+num2+" = "+(num1/num2));
		System.out.println(+num1+" % "+num2+" = "+(num1%num2));
		System.out.println(+num1+" > "+num2+" = "+(num1>num2));
		System.out.println(+num1+" < "+num2+" = "+(num1<num2));
		System.out.println(+num1+" = "+num2+" = "+(num1==num2));
	}
}